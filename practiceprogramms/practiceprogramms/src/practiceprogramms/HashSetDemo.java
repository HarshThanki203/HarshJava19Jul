package practiceprogramms;

import java.util.HashSet;

public class HashSetDemo {
	
	
	public static void main(String[] args) {
		
		HashSet hs = new HashSet();
		hs.add(10);
		hs.add(14.56);
		hs.add('t');
		hs.add("tops");
		hs.add(null);
		hs.add(10);
		hs.add(true);
		
		System.out.println(hs);
		
		
		
		
	}

}
